export type PriceRow = readonly [name: string, unit: string, price: string]

export type ContentSection = {
  h: string
  text?: string
  bullets?: string[]
  /** Optional sub-blocks rendered as nested headings + bullet lists (Kauper-style "Підлога / Стіни / Стелі") */
  groups?: { h: string; bullets: string[] }[]
}

export type BuildService = {
  slug: string
  title: string
  /** Short label used in the cards grid (already exists in BuildExpertise) */
  cardTitle: string
  /** Short pitch used in meta description and intro under H1 */
  short: string
  /** 1–2 sentences shown as hero lead */
  lead: string
  /** Long-form opening paragraph (Kauper-style "Що таке ..." section) */
  intro: string
  photo: string
  /** Optional secondary photo, falls back to `photo` */
  heroPhoto?: string
  /** Page sections beyond the intro */
  sections: ContentSection[]
  /** Optional list of object types this service is delivered for */
  objects?: string[]
  /** Why VBK — bullet points */
  why?: string[]
  /** Price table; if absent we render a CTA card with "розрахунок індивідуально" */
  priceTable?: { rows: PriceRow[]; note?: string }
}

